import java.text.*;

public class Part 
{

	// ************************************** enter fields here *************************************** //
	// ************************************************************************************************ //
		private String name;
		private String type;
		private double price; 
 	// ******************************************* end of fields ************************************** //
	// ************************************************************************************************ // 


	// ************************************ constructors here ***************************************** //
	// ************************************************************************************************ //
		public Part(String name, String type, double price)
		{
			this.name = name;
			this.type = type;
			this.price = price;
			
		}

	// ***************************************** end of constructors ********************************** //
	// ************************************************************************************************ // 
       
	

	// ***************************************** methods here ***************************************** //
	// ************************************************************************************************ //
	
        
                // ******************* accessor & mutator methods **************** //
                // *************************************************************** //
                    public String getName()
                    {
                        return name;
                    }

                    public String getType()
                    {
                        return type;
                    }

                    public double getPrice()
                    {
                        return price;
                    }

                    public void setName(String name)
                    {
                        this.name = name;
                    }

                    public void setType(String type)
                    {
                        this.type = type;
                    }

                    public void setPrice(double price)
                    {
                        this.price = price;
                    }

                // ******************* end of accessor & mutator methods ********* //
                // *************************************************************** //


                // ******************* String  methods *************************** //
                // *************************************************************** //

                        public String toString() 
                        {
                            
                            String g = type + ": " + name + " @ $" + formatted(price); 
                            return g;
                        }

                        private String formatted(double money) 
                        {
                            String k = new DecimalFormat("#####0.00").format(money);
                            return k;

                        }
                // ******************* end of string methods ********************* //
                // *************************************************************** //








                

	// ***************************************** end of methods *************************************** //
	// ************************************************************************************************ //
}